function getQuoteOfDay() {
    const now = new Date();
    const start = new Date(now.getFullYear(), 0, 0);
    const diff = now - start;
    const oneDay = 1000 * 60 * 60 * 24;
    const day = Math.floor(diff / oneDay);

    // Check local storage for "Missed Days" logic or just show today's
    // User wants: "Send notification if he also not visited on any day"
    // This implies we should show missed quotes if they missed a day?
    // For now, let's stick to the simpler "Current Day Quote" to match the 365 count.
    // If text is needed for "missed", we can add a backlog feature later.

    return loveQuotes[day % loveQuotes.length];
}

function getDailyNotification() {
    const quote = getQuoteOfDay();

    // Check if we already showed a notification TODAY
    const todayStr = new Date().toDateString();
    const lastSeen = localStorage.getItem('last_seen_date');

    if (lastSeen !== todayStr) {
        // New day, show notification!

        // 1. Try Browser Notification
        if ("Notification" in window) {
            if (Notification.permission === "granted") {
                new Notification("Daily Love Note ‚ù§Ô∏è", {
                    body: quote,
                    icon: "https://img.icons8.com/color/48/heart-puzzle.png"
                });
            } else if (Notification.permission !== "denied") {
                Notification.requestPermission().then(permission => {
                    if (permission === "granted") {
                        new Notification("Daily Love Note ‚ù§Ô∏è", {
                            body: quote,
                            icon: "https://img.icons8.com/color/48/heart-puzzle.png"
                        });
                    }
                });
            }
        }

        // 2. Show In-App Modal (Always works)
        showModal("üìÖ Daily Quote:\n\n" + quote);

        // Save today as seen
        localStorage.setItem('last_seen_date', todayStr);
    }
}

